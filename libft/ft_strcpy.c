#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char	*s;
	int		i;

	i = 0;
	s = dst;
	while (src[i] != 0)
	{
		s[i] = src[i];
		i++;
	}
	s[i] = '\0';
	return (dst);
}
