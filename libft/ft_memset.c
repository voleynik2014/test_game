#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*tmp;

	tmp = b;
	i = 0;
	while (i != len)
	{
		tmp[i] = c;
		i++;
	}
	return (b);
}
